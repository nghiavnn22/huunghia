    private void xemThongTin(String masv) {
        try {
            LocateRegistry.getRegistry();
            lbstt.setText("Get registry...");
            ClientServer cs=(ClientServer)Naming.lookup(url);
            lbstt.setText("Naming lookup "+url);
            ThongTin sv =cs.xemThongTin(masv);
            if(sv==null)
            {
                lbloi.setText("Không tìm thấy sinh viên!");
                anThongTin();
            }
            else
            {
                hienThongTin();
                lbloi.setText("");
                lbthongtin.setText("THÔNG TIN SINH VIÊN");
                txtdiachi.setText(sv.getDiachi());
                txtghichu.setText(sv.getGhichu());
                if(sv.getPhai().equals("1"))
                    txtgioitinh.setText("Nam");
                else
                    txtgioitinh.setText("Nu");
                txtmalop.setText(sv.getMalop());
                txtmsv.setText(sv.getMasv());
                txtngaysinh.setText(sv.getNgaysinh());
                if(sv.getNghihoc().equals("1"))
                    txtnghihoc.setText("Nghỉ");
                else
                    txtnghihoc.setText("Không");
                txtnoisinh.setText(sv.getNoisinh());
                txtten.setText(sv.getTen());
                txtho.setText(sv.getHo());
            }
                
        } catch (RemoteException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

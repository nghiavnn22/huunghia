    private void xemDiem(String masv) {
        try {
            LocateRegistry.getRegistry();
            ClientServer cs =(ClientServer)Naming.lookup(url);
            ArrayList<Diem> arr =cs.xemDiem(masv);
            if(arr.isEmpty())
            {
                lbloi.setText("Không tìm thấy!");
            }
            else{
                lbloi.setText("");
                eo.setVisible(true);
                lbthongtin.setText("ĐIỂM CỦA SINH VIÊN "+masv);
                DefaultTableModel model =(DefaultTableModel)jTable1.getModel();
                while(model.getRowCount()!=0)
                    model.removeRow(0);
                for(Diem i:arr){
                    model.addRow(new Object[]{i.getTenmh(),i.getHocky(),i.getLan(),i.getDiem()});
                }
                
            }
        } catch (RemoteException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

